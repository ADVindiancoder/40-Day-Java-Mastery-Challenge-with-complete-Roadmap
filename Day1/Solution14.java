package Day1;

public class Solution14 {
    
    public static void main(String[] args) {
        
        // Relational Operator

        // == <=  >= <  >  !=

        // Note: It always Gives the Answer in True And false 

        int a=20;
        int b=8;
        System.out.println(a==b); // False
        System.out.println(b==a);// False
        System.out.println(a>b);  // True
        System.out.println(b>a); // False
        System.out.println(a>=b); // True
        System.out.println(b>=a); // False
    }
}
