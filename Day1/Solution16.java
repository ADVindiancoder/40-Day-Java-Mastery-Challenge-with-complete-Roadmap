package Day1;

public class Solution16 {
    
    public static void main(String[] args) {
        
        // Ternary Operator in java 

        // Syntax (Condition) ? T : F;
        int a=210;
        int b=199;


        // biger Value will be print


        int Result=(a>b)? a:b;
        System.out.println(Result); // 210
    }
}
