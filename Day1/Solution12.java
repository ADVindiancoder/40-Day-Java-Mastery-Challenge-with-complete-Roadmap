package Day1;

public class Solution12 {
    
    public static void main(String[] args) {
        
        // Compound Assingnment Operator

        int vinay=10;

        System.out.println("The value of Vinay Variable is :"+vinay); // output = 10
        vinay=vinay+5;
        System.out.println("The value of Vinay Variable is :"+vinay); // output=15

        vinay=vinay+100;
        System.out.println(vinay);  // output 115
    }
}
