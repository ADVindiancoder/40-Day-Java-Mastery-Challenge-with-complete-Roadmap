package DAy2;

public class Solution9 {
  
    public static void main(String[] args) {
        int i = 1; // Initialize a variable i to 1
        do {
            System.out.println(i); // Print the value of i
            i++; // Increment i by 1
        } while (i <= 5); // Check if i is less than or equal to 5
    }
}
