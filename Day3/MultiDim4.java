package Day3;

public class MultiDim4 {

	public static void main(String[] args) 
	
	{
		
		int arr[][]={ 
				
				{12,85},
				{35,36},
				{85,96},
				{11,22}
				
                    };
		for(int i=0; i<arr.length;i++) {
			
			for(int j=0;j<arr[i].length;j++) {
				
				System.out.print(arr[i][j]+" ");
			}
			System.out.println();
		}
		
	
	}
}
